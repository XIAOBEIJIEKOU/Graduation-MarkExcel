package com.example.mark.bean;

/**
 * 权重
 */
public class WeightBean {

    private double homeworkWeight;

    private double testWeight;

    private double experimentWeight;

    private double examinationWeight;

    public double getHomeworkWeight() {
        return homeworkWeight;
    }

    public void setHomeworkWeight(double homeworkWeight) {
        this.homeworkWeight = homeworkWeight;
    }

    public double getTestWeight() {
        return testWeight;
    }

    public void setTestWeight(double testWeight) {
        this.testWeight = testWeight;
    }

    public double getExperimentWeight() {
        return experimentWeight;
    }

    public void setExperimentWeight(double experimentWeight) {
        this.experimentWeight = experimentWeight;
    }

    public double getExaminationWeight() {
        return examinationWeight;
    }

    public void setExaminationWeight(double examinationWeight) {
        this.examinationWeight = examinationWeight;
    }
}
